const prepareInput = (input: string) => {
  const lines = input.split("\n");
  const index = parseInt(lines[0]);
  return index;
};

export const resolution = (input: string) => {
  const index = prepareInput(input);
  const listOfMonths = [
    "January",
    "February",
    "March",
    "April",
    "May",
    "June",
    "July",
    "August",
    "September",
    "October",
    "November",
    "December",
  ];

  return listOfMonths[index - 1];
};

console.log(resolution(input));
